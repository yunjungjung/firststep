package com.itwill.intro; // 패키지 선언문

/*
 * block comment 
 * 주로 문서화(documentation) 주석으로 사용됨.
 * 클래스 설명, 메서드 설명, ...
 */
public class IntroMain { // 클래스 선언부

    public static void main(String[] args) { // 메인 메서드 선언 시작
        // 메인 메서드: 자바 프로그램의 시작
        // inline 주석(comment): 프로그램 코드에 대한 설명.
        System.out.println("Hello!!!");
        System.out.println("안녕하세요~ 여러분!");
    } // 메인 메서드 선언 끝 -> 자바 프로그램 종료

} // 클래스 선언 끝